function changeLanguage(lang) {
    localStorage.setItem('selectedLanguage', lang);
    updatePageLanguage(lang);

    // Refresh history or prediction results if they exist on the page
    if (window.fetchHistory) fetchHistory();
}

function updatePageLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update document title
    if (translations[lang] && translations[lang].title) {
        document.title = "🌾 " + translations[lang].title;
    }
}

// Helper to translate dynamic AI results
function translateResult(key, type = 'prediction') {
    const lang = localStorage.getItem('selectedLanguage') || 'en';
    const dict = translations[lang];
    if (type === 'solution') {
        return dict['sol_' + key] || key;
    }
    return dict[key] || key;
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'en';

    const navBar = document.querySelector('.nav-bar');
    if (navBar && !document.getElementById('lang-select')) {
        const langDiv = document.createElement('div');
        langDiv.style.marginLeft = 'auto';
        langDiv.style.marginRight = '20px';
        langDiv.style.display = 'flex';
        langDiv.style.alignItems = 'center';
        langDiv.style.gap = '10px';

        langDiv.innerHTML = `
            <select id="lang-select" onchange="changeLanguage(this.value)">
                <option value="en" ${savedLang === 'en' ? 'selected' : ''}>English</option>
                <option value="ta" ${savedLang === 'ta' ? 'selected' : ''}>தமிழ்</option>
                <option value="si" ${savedLang === 'si' ? 'selected' : ''}>සිංහල</option>
            </select>
        `;
        navBar.appendChild(langDiv);
    }

    updatePageLanguage(savedLang);
});
